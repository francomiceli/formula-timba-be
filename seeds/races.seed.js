export async function seedRaces() {
  let created = 0;
  let existing = 0;

  for (const race of racesSeed) {
    const [instance, wasCreated] = await Race.findOrCreate({
      where: { 
        season: race.season, 
        round: race.round 
      },
      defaults: race,
    });

    if (wasCreated) {
      created++;
    } else {
      existing++;
    }
  }

  console.log(`✅ Races seed: ${created} creadas, ${existing} ya existían`);
}

